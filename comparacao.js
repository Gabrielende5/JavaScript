// um igual significa atribuição 
var numero = 12
const texto = "12"
// dois iguais é uma pergunta de conteúdo sem tipagem 
console.log(texto ==  numero);
// tres iguais verifica-se o conteúdo é o mesmo e também checa a tipagem
console.log(texto === numero);
// dois iguais e uma esclamação checam se é diferente o conteúdo
console.log(texto !== numero);